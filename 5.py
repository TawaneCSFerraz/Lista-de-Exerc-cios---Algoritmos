#    5. Média de Notas 
# Peça ao usuário para digitar três notas e calcule a média delas.


nota1 = float(input("Digite sua 1º nota: "))
nota2 = float(input("Digite sua 2º nota: "))
nota3 = float(input("Digite sua 2º nota: "))

conta = (nota1 + nota2 + nota3) / 3

resultado = print(f"Sua média foi de: {conta}")

